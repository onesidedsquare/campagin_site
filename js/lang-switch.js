const supportedLangs = ['en', 'es'];
const defaultLang = 'en';

function getPreferredLanguage() {
    const params = new URLSearchParams(window.location.search);
    const queryLang = params.get('lang');
    if (queryLang && supportedLangs.includes(queryLang)) {
        return queryLang;
    }
    const storedLang = window.localStorage.getItem('siteLang');
    if (storedLang && supportedLangs.includes(storedLang)) {
        return storedLang;
    }
    return defaultLang;
}

function updateUrlLang(lang) {
    try {
        const url = new URL(window.location.href);
        url.searchParams.set('lang', lang);
        history.replaceState({}, '', url.toString());
    } catch (error) {
        // ignore URL update failures on older browsers
    }
}

function translatePage(lang) {
    const map = translations[lang] || translations[defaultLang];
    document.documentElement.lang = lang;
    const elements = document.querySelectorAll('[data-translate-key]');
    elements.forEach(el => {
        const key = el.dataset.translateKey;
        if (!key) return;
        const translation = map[key];
        if (translation !== undefined) {
            el.textContent = translation;
        }
    });
    const placeholderElements = document.querySelectorAll('[data-translate-placeholder-key]');
    placeholderElements.forEach(el => {
        const key = el.dataset.translatePlaceholderKey;
        if (!key) return;
        const translation = map[key];
        if (translation !== undefined) {
            el.setAttribute('placeholder', translation);
        }
    });
    applyStaticTextTranslations(lang);
    applyMetaTranslations(lang);
    _updateToggleButton(lang);
    updateUrlLang(lang);
    window.localStorage.setItem('siteLang', lang);
}

function normalizeText(value) {
    return String(value || '').replace(/\s+/g, ' ').trim();
}

function applyStaticTextTranslations(lang) {
    const phraseMap = staticTextTranslations[lang] || {};

    document.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(el => {
        if (!el.dataset.basePlaceholder) {
            el.dataset.basePlaceholder = el.getAttribute('placeholder') || '';
        }
        const base = el.dataset.basePlaceholder;
        if (lang === defaultLang) {
            el.setAttribute('placeholder', base);
        } else if (phraseMap[base]) {
            el.setAttribute('placeholder', phraseMap[base]);
        }
    });

    const candidates = document.querySelectorAll('h1,h2,h3,h4,h5,p,a,li,label,small,button,span');
    candidates.forEach(el => {
        if (el.dataset.translateKey) return;

        if (!el.dataset.baseText) {
            el.dataset.baseText = normalizeText(el.textContent);
        }

        const base = el.dataset.baseText;
        if (!base) return;

        if (lang === defaultLang) {
            if (el.childElementCount === 0) {
                el.textContent = base;
            }
            return;
        }

        const translated = phraseMap[base];
        if (!translated) return;

        if (el.childElementCount === 0) {
            el.textContent = translated;
        }
    });
}

function applyMetaTranslations(lang) {
    const path = window.location.pathname.split('/').pop() || 'index.html';
    if (lang === defaultLang) return;

    const page = pageMetaTranslations[lang] && pageMetaTranslations[lang][path];
    if (!page) return;

    if (!document.documentElement.dataset.baseTitle) {
        document.documentElement.dataset.baseTitle = document.title;
    }
    document.title = page.title || document.documentElement.dataset.baseTitle;

    const metaDescription = document.querySelector('meta[name="description"]');
    if (!metaDescription) return;
    if (!metaDescription.dataset.baseDescription) {
        metaDescription.dataset.baseDescription = metaDescription.getAttribute('content') || '';
    }
    metaDescription.setAttribute('content', page.description || metaDescription.dataset.baseDescription);
}

function _updateToggleButton(lang) {
    const btn = document.getElementById('lang-toggle-btn');
    if (!btn) return;
    btn.textContent = lang === 'en' ? 'Español' : 'English';
    btn.setAttribute('aria-label', lang === 'en' ? 'Cambiar a Español' : 'Switch to English');
}

function initLanguageSwitcher() {
    const lang = getPreferredLanguage();
    translatePage(lang);
    _updateToggleButton(lang);
    document.querySelectorAll('a[target="_blank"]').forEach(link => {
        link.setAttribute('rel', 'noopener noreferrer');
    });
    const toggleBtn = document.getElementById('lang-toggle-btn');
    if (toggleBtn) {
        toggleBtn.addEventListener('click', () => {
            const current = window.localStorage.getItem('siteLang') || defaultLang;
            const next = current === 'en' ? 'es' : 'en';
            translatePage(next);
            _updateToggleButton(next);
        });
    }
}

document.addEventListener('DOMContentLoaded', initLanguageSwitcher);
